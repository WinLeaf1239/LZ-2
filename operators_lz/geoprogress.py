b = float(input('Введите значение первого элемента прогрессии от -10_000 до 10_000 включительно '))
q = float(input('Введите значение множителя прогресии от 1 до 50 включительно '))
n = float(input('Введите номер последнего эл-та прогресии от 2 до 100 включительно '))
s = (b * (1 - (q ** n))) / (1 - q)
print("Сумма геометрической прогресии по заданным вами значениям:" , s)